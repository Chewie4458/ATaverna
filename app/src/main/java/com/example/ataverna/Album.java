package com.example.ataverna;

public class Album {
    private String nome;
    private String artista;

    public Album() {}

    public String getNome()
    {
        return nome;
    }
    public void setNome(String nome)
    {
        this.nome = nome;
    }

    public String getArtista()
    {
        return artista;
    }
    public void setArtista(String artista)
    {
        this.artista = artista;
    }
}
